#include "3-calc.h"
/**
 * op_add - returns the sum of a and b
 * @a: an int
 * @b: another int
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference of a and b
 * @a: an int
 * @b: another int
 * Return: the difference 
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product of a and b
 * @a: an int
 * @b: another int
 * Return: The product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the result of the division
 * of a by b
 * @a: an int
 * @b: another int
 * Return: Division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder of a division
 * @a: an int
 * @b: another int
 * Return: The remainder, aka modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
